// Exercise 1: Numbers
let intNum = 14;
let floatNum = 5.5;

console.log("Addition:", intNum + floatNum);
console.log("Subtraction:", intNum - floatNum);
console.log("Multiplication:", intNum * floatNum);
console.log("Division:", intNum / floatNum);
console.log("Modulus:", intNum % floatNum);

// Exercise 2: Boolean and Operators
const a = 8;
const b = 12;

console.log("a > b:", a > b);
console.log("a < b:", a < b);
console.log("a >= b:", a >= b);
console.log("a <= b:", a <= b);
console.log("a == b:", a == b);
console.log("a != b:", a != b);

let x = true, y = false;

console.log("x && y:", x && y);
console.log("x || y:", x || y);
console.log("!x:", !x);

// Exercise 3: Compound Assignment
let result = 10;

result += 5;
console.log("+=:", result);

result -= 3;
console.log("-=:", result);

result *= 2;
console.log("*=:", result);

result /= 4;
console.log("/=:", result);

result %= 3;
console.log("%=:", result);
